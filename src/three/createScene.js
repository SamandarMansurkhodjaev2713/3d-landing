import * as THREE from "three";

// Сцена с прозрачным фоном — тёмный backdrop рисует CSS под canvas.
// Лёгкий fog добавляет глубину и мягко прячет дальние звёзды.
export function createScene() {
  const scene = new THREE.Scene();
  scene.background = null;
  // Туман уводит грунт в горизонт. Цвет заметно приподнят над фоном неба, чтобы на
  // стыке возникала читаемая светлая линия горизонта (грунт светлее чёрного космоса).
  // Цвет тумана = цвет нижней части купола неба (createSky) → грунт бесшовно
  // растворяется в горизонте, без резкой линии стыка земля/небо.
  scene.fog = new THREE.FogExp2(0x1b1a22, 0.02);
  return scene;
}
